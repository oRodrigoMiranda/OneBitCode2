import math

# 1 = Acessar o numero pi.
print(math.pi)
print(f"{math.pi:.2f}")

# 2 = Acessar o numero de Euler
print(math.e)
print(f"{math.e:.2f}")

num1 = 10.45
print(math.ceil(num1))
print(math.floor(num1))

#4 - fatorial
num2 = 7
print(math.factorial(num2))

# potencia de numeros
print(math.pow(5,2))

# Raiz quadrada
print(math.sqrt(169))

# MDC
print(math.gcd(20,70))

# Logaritmo
print(math.log(10))